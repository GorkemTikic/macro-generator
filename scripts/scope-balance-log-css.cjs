// One-shot CSS transformer.
// Reads the upstream balance-log-analyzer's styles.css (sibling project) and
// writes a copy under src/features/balance-log/balance-log.css where every
// selector is scoped under `.balance-log-app`, so its global-looking classes
// (.container, .header, .btn, .tab, .card, .kpi, ...) cannot collide with
// macro-generator's existing global stylesheet.
//
// Lives outside src/ to keep Vite's dependency optimizer from scanning it.
//
// Run: `node scripts/scope-balance-log-css.cjs`
//
// Rules:
//   :root { ... }                  -> .balance-log-app { ... }
//   html, body { ... }             -> dropped (don't touch macro-generator chrome)
//   #root { ... }                  -> dropped
//   *, *::before, *::after { ... } -> dropped (already covered globally)
//   .foo, .bar { ... }             -> .balance-log-app .foo, .balance-log-app .bar { ... }
//   @media (...) { .foo { ... } }  -> @media (...) { .balance-log-app .foo { ... } }
//   @keyframes / comments          -> kept verbatim
const fs = require("fs");
const path = require("path");

// Resolve from this script's location instead of an absolute path so the
// transformer works regardless of where the project is checked out.
// __dirname = .../macro-generator-main/macro-generator-main/scripts
// Climb four levels to reach the user's Desktop/, then enter the sibling
// Balance Log/ project. Adjust if you move the upstream project.
const SRC = path.resolve(
  __dirname,
  "../../../../Balance Log/balance-log-analyzer-main/balance-log-analyzer-main/src/styles.css"
);
const DST = path.resolve(__dirname, "../src/features/balance-log/balance-log.css");

const SCOPE = ".balance-log-app";

function tokenize(css) {
  // Strip /* ... */ comments first; we re-emit a banner up top.
  return css.replace(/\/\*[\s\S]*?\*\//g, "");
}

function scopeSelectorList(selectorList) {
  // Split by commas not inside parentheses (safe here — no commas in pseudo args used).
  return selectorList
    .split(",")
    .map((sel) => sel.trim())
    .filter(Boolean)
    .map((sel) => {
      if (sel === ":root") return SCOPE;
      // Drop selectors that target the host page chrome.
      if (/^(html|body|#root|\*)(\b|$)/.test(sel)) return null;
      // Already scoped — leave alone.
      if (sel.startsWith(SCOPE)) return sel;
      return `${SCOPE} ${sel}`;
    })
    .filter(Boolean)
    .join(",\n");
}

// Walk top-level rules with a tiny brace-aware scanner.
function transform(css) {
  const out = [];
  let i = 0;
  const n = css.length;

  while (i < n) {
    // Skip whitespace
    while (i < n && /\s/.test(css[i])) i++;
    if (i >= n) break;

    // @-rules
    if (css[i] === "@") {
      // Find prelude end (`{` or `;`)
      let j = i;
      while (j < n && css[j] !== "{" && css[j] !== ";") j++;
      const prelude = css.slice(i, j);
      const preludeName = prelude.match(/^@([\w-]+)/)?.[1] ?? "";

      if (j < n && css[j] === ";") {
        // @charset, @import, etc.
        out.push(prelude + ";\n");
        i = j + 1;
        continue;
      }

      // Block @-rule
      const blockStart = j; // at '{'
      let depth = 0;
      let k = blockStart;
      for (; k < n; k++) {
        if (css[k] === "{") depth++;
        else if (css[k] === "}") {
          depth--;
          if (depth === 0) {
            k++;
            break;
          }
        }
      }
      const blockBody = css.slice(blockStart + 1, k - 1);

      if (preludeName === "media" || preludeName === "supports" || preludeName === "container") {
        // Recurse into inner rules.
        out.push(prelude + "{\n" + transform(blockBody) + "}\n\n");
      } else {
        // @keyframes / @font-face etc — keep verbatim.
        out.push(prelude + "{" + blockBody + "}\n\n");
      }
      i = k;
      continue;
    }

    // Regular rule: selector { decls }
    let j = i;
    let depth = 0;
    while (j < n) {
      if (css[j] === "{") {
        if (depth === 0) break;
        depth++;
      } else if (css[j] === "}") {
        depth--;
      }
      j++;
    }
    if (j >= n) break;

    const selectors = css.slice(i, j).trim();
    let k = j + 1;
    let braceDepth = 1;
    while (k < n && braceDepth > 0) {
      if (css[k] === "{") braceDepth++;
      else if (css[k] === "}") braceDepth--;
      if (braceDepth > 0) k++;
    }
    const decls = css.slice(j + 1, k);

    const scoped = scopeSelectorList(selectors);
    if (scoped) {
      out.push(scoped + " {" + decls + "}\n\n");
    }
    i = k + 1;
  }

  return out.join("");
}

const banner = `/*
 * Balance Log Analyzer styles, scoped under \`${SCOPE}\`.
 *
 * Generated by scripts/scope-balance-log-css.cjs from the upstream
 * balance-log-analyzer styles.css. Do not edit by hand — for tweaks layer
 * additions in src/features/balance-log/balance-log-overrides.css instead.
 * Re-run the script when upstream styles change.
 */
`;

const src = fs.readFileSync(SRC, "utf8");
const out = banner + transform(tokenize(src));
fs.writeFileSync(DST, out, "utf8");
console.log(`Wrote ${DST} (${out.length} bytes)`);
